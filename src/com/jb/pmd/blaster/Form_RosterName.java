    private void returnRoster()
    {
     String roster = jTextField1.getText();
        
        if(BlasterEngine.getRosters().contains(roster))
            javax.swing.JOptionPane.showMessageDialog(null, 
                    roster + " already exists.");
        
        else
        {
            rosterName = roster;
            dispose();
        }
    }

