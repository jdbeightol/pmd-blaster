    public static Contact getContact(java.awt.Frame parent, Contact defaultValue)
    {
        Form_Contact a = new Form_Contact(parent, true);
        
        if(defaultValue != null)
        {
            a.id = defaultValue.id;
            a.jTextField1.setText(defaultValue.first);
            a.jTextField2.setText(defaultValue.last);
            a.jTextField3.setText(defaultValue.phone);
            a.jTextField4.setText(defaultValue.email);
        }
        
        a.setVisible(true);
        
        return a.contact;
    }

